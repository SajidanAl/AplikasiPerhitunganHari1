    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // Get selected month and year
    String monthStr = (String) jComboBox1.getSelectedItem();
    int month = getMonthNumber(monthStr);  // Convert month name to number

    // Extract year from the spinner (which returns a Date)
    Date selectedDate = (Date) jSpinner1.getValue();
    Calendar calendar = Calendar.getInstance();
    calendar.setTime(selectedDate);
    int year = calendar.get(Calendar.YEAR); // Extract the year

    // Get the first and last date of the selected month and year
    YearMonth yearMonth = YearMonth.of(year, month);
    int daysInMonth = yearMonth.lengthOfMonth(); // Get the number of days in the month

    LocalDate firstDay = yearMonth.atDay(1); // First day of the month
    LocalDate lastDay = yearMonth.atDay(daysInMonth); // Last day of the month

    // Display first and last day in text fields
    Hpertama.setText(firstDay.format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));
    Hkedua.setText(lastDay.format(DateTimeFormatter.ofPattern("dd-MM-yyyy")));

    // Display total number of days in the month
    hasil.setText(String.valueOf(daysInMonth));

    // Optionally, handle leap year check for February
    if (month == 2 && yearMonth.isLeapYear()) {
        JOptionPane.showMessageDialog(this, "Tahun " + year + " adalah tahun kabisat (leap year).");
    }
    }//GEN-LAST:event_btnHitungActionPerformed
    
    
    
